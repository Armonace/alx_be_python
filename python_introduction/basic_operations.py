# basic_operations.py

number1 = 10
number2 = 5

# addition
sum = number1 + number2

# subtraction
difference = number1 - number2

# multiplication
product = number1 * number2

# division
quotient = number1 / number2

# print results
print("Addition of", number1, "and", number2, "is", sum)
print("Subtraction of", number1, "and", number2, "is", difference)
print("Multiplication of", number1, "and", number2, "is", product)
print("Division of", number1, "by", number2, "is", quotient)
